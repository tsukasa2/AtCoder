#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;

  // 以下のコメント/* */を消して追記する
  cout << /* 1年は何秒か */ seconds << endl;
  cout << /* 2年は何秒か */ seconds * 2 << endl;
  cout << /* 5年は何秒か */ seconds * 5 << endl;
  cout << /* 10年は何秒か */ seconds * 10 << endl;
}
